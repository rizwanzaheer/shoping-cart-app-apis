const express = require('express');

// Middleware
const validate = require('../../middlewares/validate');
const { upload } = require('../../middlewares/fileUpload');

// Validators
const { productValidation } = require('../../validations');

// Controllers
const { productController } = require('../../controllers');

const router = express.Router();

router
  .route('/')
  .post(validate(productValidation.createProduct), upload.single('file'), productController.createProduct)
  .get(validate(productValidation.getProducts), productController.getProducts);

router.route('/search').get(validate(productValidation.searchProductByName), productController.searchProductByProductName);

router
  .route('/:productId')
  .get(validate(productValidation.getProduct), productController.getProduct)
  .patch(validate(productValidation.updateProduct), productController.updateProduct)
  .delete(validate(productValidation.deleteProduct), productController.deleteProduct);

module.exports = router;

